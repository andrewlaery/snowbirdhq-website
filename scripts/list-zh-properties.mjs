#!/usr/bin/env node
// list-zh-properties.mjs
//
// Scans content/docs/zh/properties/<slug>/ and writes the list of ZH-active
// slugs to src/lib/zh-properties.ts. The LocaleSwitcher imports the
// generated array so the locale-switcher pill links straight to the
// property's ZH page when a translation exists, instead of bouncing the
// user to the locale root.
//
// Wired into npm run prebuild so the list is always fresh on Vercel
// builds. Also called from scripts/scaffold-zh-property.mjs after each
// scaffold run so local dev sees the new entry without manual steps.
//
// Idempotent — safe to run any number of times.

import { readdirSync, writeFileSync, existsSync, statSync } from 'node:fs';
import { join, resolve, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const ZH_PROPERTIES_DIR = join(REPO, 'content', 'docs', 'zh', 'properties');
const OUTPUT_PATH = join(REPO, 'src', 'lib', 'zh-properties.ts');

function listZhSlugs() {
  if (!existsSync(ZH_PROPERTIES_DIR)) return [];
  return readdirSync(ZH_PROPERTIES_DIR)
    .filter((name) => {
      if (name.startsWith('.')) return false;
      const path = join(ZH_PROPERTIES_DIR, name);
      return statSync(path).isDirectory();
    })
    .sort();
}

function emitFile(slugs) {
  const list = slugs.map((s) => `  '${s}',`).join('\n');
  return `// AUTO-GENERATED by scripts/list-zh-properties.mjs. Do not edit by hand.
// Regenerated whenever a ZH property is scaffolded, and on \`npm run prebuild\`.
//
// Source of truth: directory listing of content/docs/zh/properties/<slug>/.
// To add a slug: run \`node scripts/scaffold-zh-property.mjs <slug>\` (which
// also calls this script). To remove a slug: delete the directory and re-run.

export const ZH_TRANSLATED_SLUGS: ReadonlySet<string> = new Set([
${list}
]);
`;
}

function main() {
  const slugs = listZhSlugs();
  const content = emitFile(slugs);
  writeFileSync(OUTPUT_PATH, content, 'utf-8');
  console.log(
    `[list-zh-properties] ${slugs.length} ZH ${slugs.length === 1 ? 'property' : 'properties'} → ${relative(REPO, OUTPUT_PATH)}`,
  );
}

main();
