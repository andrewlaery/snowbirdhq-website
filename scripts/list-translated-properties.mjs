#!/usr/bin/env node
// list-translated-properties.mjs
//
// Scans content/docs/<lang>/properties/<slug>/ for every supported locale and
// writes one TS module per locale at src/lib/<lang>-properties.ts. The
// LocaleSwitcher imports each module so the pill links straight to the
// property's translated page when one exists, instead of bouncing the user
// to the locale root.
//
// Wired into npm run prebuild so the lists are always fresh on Vercel
// builds. Also called from scripts/scaffold-zh-property.mjs after each
// scaffold run so local dev sees the new entry without manual steps.
//
// Idempotent — safe to run any number of times.

import { readdirSync, writeFileSync, existsSync, statSync } from 'node:fs';
import { join, resolve, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO = resolve(dirname(fileURLToPath(import.meta.url)), '..');

// Mirrors LOCALES in src/middleware.ts — adding a locale here scaffolds the
// generated TS module the LocaleSwitcher reads.
const LOCALES = ['zh', 'ja'];

function listSlugs(lang) {
  const dir = join(REPO, 'content', 'docs', lang, 'properties');
  if (!existsSync(dir)) return [];
  return readdirSync(dir)
    .filter((name) => {
      if (name.startsWith('.')) return false;
      const path = join(dir, name);
      return statSync(path).isDirectory();
    })
    .sort();
}

function emitFile(lang, slugs) {
  const upper = lang.toUpperCase();
  const list = slugs.map((s) => `  '${s}',`).join('\n');
  return `// AUTO-GENERATED by scripts/list-translated-properties.mjs. Do not edit by hand.
// Regenerated whenever a property is scaffolded for any locale, and on
// \`npm run prebuild\`.
//
// Source of truth: directory listing of content/docs/${lang}/properties/<slug>/.
// To add a slug: run \`node scripts/scaffold-zh-property.mjs <slug> --lang=${lang}\`
// (which also calls this script). To remove a slug: delete the directory and re-run.

export const ${upper}_TRANSLATED_SLUGS: ReadonlySet<string> = new Set([
${list}
]);
`;
}

function main() {
  let total = 0;
  for (const lang of LOCALES) {
    const slugs = listSlugs(lang);
    const outputPath = join(REPO, 'src', 'lib', `${lang}-properties.ts`);
    const content = emitFile(lang, slugs);
    writeFileSync(outputPath, content, 'utf-8');
    console.log(
      `[list-translated] ${slugs.length} ${lang} ${slugs.length === 1 ? 'property' : 'properties'} → ${relative(REPO, outputPath)}`,
    );
    total += slugs.length;
  }
  console.log(
    `[list-translated] ${total} translation(s) across ${LOCALES.length} locale(s)`,
  );
}

main();
